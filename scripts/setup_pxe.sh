# setup_pxe.sh
# Este script automatiza la configuración del servidor PXE para permitir la instalación desatendida de sistemas en clientes.
# Incluye la instalación de servicios necesarios (como DHCP y TFTP), la configuración de las imágenes de instalación y la preparación de la red para el arranque PXE de los clientes.
# Es esencial para configurar el entorno de despliegue masivo de sistemas operativos.
