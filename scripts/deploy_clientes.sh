# deploy_clientes.sh
# Este script automatiza el proceso de despliegue de las imágenes de sistema a los clientes a través de PXE.
# Se encarga de preparar los servidores y equipos cliente para recibir la instalación desatendida y configurar las opciones básicas del sistema.
# El script maneja la configuración del servidor PXE, la actualización de las imágenes y la gestión del arranque de los clientes.
